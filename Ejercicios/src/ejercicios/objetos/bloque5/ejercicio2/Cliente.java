package ejercicios.objetos.bloque5.ejercicio2;

public class Cliente {

	String nombre;
	int id;
	
	public Cliente(String nombre, int id) {
		this.nombre = nombre;
		this.id = id;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	
	
}
