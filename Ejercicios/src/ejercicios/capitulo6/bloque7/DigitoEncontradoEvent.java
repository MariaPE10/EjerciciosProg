public class DigitoEncontradoEvent {
	
	private String palabra;

	/**
	 * @param palabra
	 */
	public DigitoEncontradoEvent(String palabra) {
		super();
		this.palabra = palabra;
	}

	/**
	 * @return the palabra
	 */
	public String getPalabra() {
		return palabra;
	}
	
	

}
